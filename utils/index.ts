export const generateCarImageUrl = async (car: CarProps) => {
  const { make, model, year } = car;

  // The URL for the Car Imagery API
  const carImageryUrl = new URL("https://www.carimagery.com/api.asmx/GetImageUrl");
  carImageryUrl.searchParams.append("searchTerm", `${make} ${model}`);

  try {
    // Fetch the XML response
    const response = await fetch(carImageryUrl.toString());
    const xmlText = await response.text();

    // Parse the XML response to extract the image URL
    const parser = new DOMParser();
    const xmlDoc = parser.parseFromString(xmlText, "text/xml");
    const imageUrl = xmlDoc.getElementsByTagName("string")[0]?.textContent;

    return imageUrl || "";
  } catch (error) {
    console.error("Error fetching car image URL:", error);
    return "";
  }
};

